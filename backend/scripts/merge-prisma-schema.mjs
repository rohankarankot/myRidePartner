import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const prismaDir = path.resolve('prisma');
const schemaDir = path.join(prismaDir, 'schema');
const outputFile = path.join(prismaDir, 'schema.prisma');

async function main() {
  const entries = await readdir(schemaDir);
  const files = entries.filter((entry) => entry.endsWith('.prisma')).sort();

  if (files.length === 0) {
    throw new Error(`No Prisma schema parts found in ${schemaDir}`);
  }

  const sections = await Promise.all(
    files.map(async (file) => {
      const content = await readFile(path.join(schemaDir, file), 'utf8');
      return `// --- ${file} ---\n${content.trim()}`;
    }),
  );

  const output = [
    '// This file is generated by `npm run prisma:merge`.',
    '// Edit files under `prisma/schema/` instead of editing this file directly.',
    '',
    sections.join('\n\n'),
    '',
  ].join('\n');

  await writeFile(outputFile, output, 'utf8');
  console.log(`Merged ${files.length} Prisma schema parts into ${path.relative(process.cwd(), outputFile)}.`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
